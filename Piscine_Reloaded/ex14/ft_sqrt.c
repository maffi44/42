int	ft_sqrt(int nb)
{
	int i;

	if (nb > 0)
	{
		i = 1;
		while (i <= nb / 2)
		{
			if (i > 46340)
				break ;
			if (i * i == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
